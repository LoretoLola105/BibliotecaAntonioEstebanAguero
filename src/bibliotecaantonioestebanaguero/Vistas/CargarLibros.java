    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
       try {
    String isbnText = jTIsbn.getText();
    
    if (isbnText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "El número de ISBN es autoincremental y se generará automáticamente.");
        jTIsbn.setToolTipText("El número de ISBN es autoincremental y se generará automáticamente.");
        return; // Finaliza la ejecución de la función si el ISBN está vacío.
    }                    
    
        Integer isbn = Integer.parseInt(jTIsbn.getText());
        String titulo = jTtitulo.getText().toLowerCase();
        String autor = jTautor.getText().toLowerCase();
        int anio = Integer.parseInt(jTanio.getText()+"");
        String genero = jTgenero.getText().toLowerCase();
        String editorial = jTeditorial.getText().toLowerCase();
        Boolean estado = jCheckBestado.isSelected();

        if (titulo.isEmpty() || autor.isEmpty() || genero.isEmpty() || editorial.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No pueden existir campos vacíos.");
            return; // Finaliza la ejecución de la función si hay campos vacíos.
        }

        if (libroActual == null) {
            libroActual = new Libro(titulo, autor, anio, genero, editorial, estado);
            libData.guardarLibro(libroActual);
        } else {
            libroActual.setIsbn(isbn);
            libroActual.setTitulo(titulo);
            libroActual.setAutor(autor);
            libroActual.setAnio(anio);
            libroActual.setGenero(genero);
            libroActual.setEditorial(editorial);
            libroActual.setEstado(true);
            libData.modificarLibro(lib);
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Debe ingresar un número de ISBN válido.");
        // Muestra el ToolTip en caso de error de formato
        jTIsbn.setToolTipText("El número de ISBN es autoincremental y se generará automáticamente.");
        return; // Finaliza la ejecución de la función si hay un error de formato.
    
}

    }//GEN-LAST:event_jBGuardarActionPerformed

    private void jTIsbnKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTIsbnKeyTyped
        // TODO add your handling code here:
        char key = evt.getKeyChar();
    boolean numero = key >= '0' && key <= '9';
    
    if (!numero) {
        evt.consume();
        JOptionPane.showMessageDialog(this, "Solo se aceptan números naturales.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    if (jTIsbn.getText().trim().length() == 10) {
        evt.consume();
        JOptionPane.showMessageDialog(this, "El ISBN no debe exceder los 10 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jTIsbnKeyTyped

