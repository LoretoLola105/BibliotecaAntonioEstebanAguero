    private void jTautorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTautorKeyTyped
        // TODO add your handling code here:
           if (!(Character.isLetter(evt.getKeyChar())) && !(evt.getKeyChar() == KeyEvent.VK_SPACE)) {
        evt.consume(); // Consume el carácter no válido
        // Muestra el mensaje de error solo una vez
        JOptionPane.showMessageDialog(this, "Solo se aceptan letras para introducir el nombre del Autor.", "Error", JOptionPane.ERROR_MESSAGE); 
    }
    }//GEN-LAST:event_jTautorKeyTyped

