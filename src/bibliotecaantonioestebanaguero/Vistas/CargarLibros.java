    private void jBbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBbuscarActionPerformed
        // TODO add your handling code here:
         try {
            Integer isbn = Integer.parseInt(jTIsbn.getText());
            libroActual = libData.buscarLibroPorIdLibro(isbn);
            if (libroActual != null) {
                jTtitulo.setText(libroActual.getTitulo().toLowerCase());
                jTautor.setText(libroActual.getAutor().toLowerCase());
                jTanio.setText(libroActual.getAnio() + "");
                jTgenero.setText(libroActual.getGenero().toLowerCase());
                jTeditorial.setText(libroActual.getEditorial().toLowerCase());
                jCheckBestado.setSelected(libroActual.isEstado());

            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero de ID ISBN válido. ");
        }


    }//GEN-LAST:event_jBbuscarActionPerformed

