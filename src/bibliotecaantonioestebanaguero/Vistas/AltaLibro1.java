    private void BUSCARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUSCARActionPerformed
         try {
            Integer isbn = Integer.parseInt(jTidIsbn.getText());
            libroActual = libData.buscarLibroPorIdLibro(isbn);
            if (libroActual != null) {
                jTtitulo.setText(libroActual.getTitulo().toLowerCase());
                jTautor.setText(libroActual.getAutor().toLowerCase());
                jTanio.setText(libroActual.getAnio() + "");
                jTgenero.setText(libroActual.getGenero().toLowerCase());
                jTeditorial.setText(libroActual.getEditorial().toLowerCase());
                CheckBoxESTADO.setSelected(libroActual.isEstado());

            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero de ID ISBN vÃ¡lido. ");
        }
    }//GEN-LAST:event_BUSCARActionPerformed

    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GUARDARActionPerformed
        // TODO add your handling code here:
     try {
            Integer isbn = Integer.parseInt(jTidIsbn.getText());
            String titulo = jTtitulo.getText().toLowerCase();
            String autor = jTautor.getText().toLowerCase();
            int anio = Integer.parseInt(jTanio.getText()+"");
            String genero = jTgenero.getText().toLowerCase();
            String editorial = jTeditorial.getText().toLowerCase();
            Boolean estado = CheckBoxESTADO.isSelected();
            
            
        if (titulo.isEmpty() || autor.isEmpty() || genero.isEmpty() || editorial.isEmpty())  {
             JOptionPane.showMessageDialog(this, "No pueden existir campos vacÃ­os.");
                  
                return;
      }

            if (libroActual == null) {

                libroActual = new Libro(titulo, autor, anio, genero, editorial, estado);
                libData.guardarLibro(libroActual);

            } else {
                libroActual.setIsbn(isbn);
                libroActual.setTitulo(titulo);
                libroActual.setAutor(autor);
                libroActual.setAnio(anio);
                libroActual.setGenero(genero);
                libroActual.setEditorial(editorial);
                libroActual.setEstado(true);
                libData.modificarLibro(lib);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un numero de ISBN vÃ¡lido. ");
        }

    }//GEN-LAST:event_GUARDARActionPerformed

